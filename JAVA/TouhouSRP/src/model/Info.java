package model;

public class Info {

	private int id;  //id 정보
	private String name;  //이름
	
	protected Info() {}
	protected Info(int id, String name) {
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
